router.get("/analysis/stats", async (req, res) => {
  try {
    const [totals] = await db
      .select({ totalAnalyses: count(), avgSuccessProbability: avg(analysesTable.successProbability) })
      .from(analysesTable);
    const [buyStats] = await db
      .select({ count: count(), avg: avg(analysesTable.successProbability) })
      .from(analysesTable)
      .where(and(eq(analysesTable.tradeType, "buy"), ne(analysesTable.successProbability, 0)));
    const [sellStats] = await db
      .select({ count: count(), avg: avg(analysesTable.successProbability) })
      .from(analysesTable)
      .where(and(eq(analysesTable.tradeType, "sell"), ne(analysesTable.successProbability, 0)));
    const marketRows = await db.select({ market: analysesTable.market, cnt: count() }).from(analysesTable).groupBy(analysesTable.market);
    const timeframeRows = await db.select({ timeframe: analysesTable.timeframe, cnt: count() }).from(analysesTable).groupBy(analysesTable.timeframe);
    const marketBreakdown: Record<string, number> = {};
    for (const r of marketRows) marketBreakdown[r.market] = r.cnt;
    const timeframeBreakdown: Record<string, number> = {};
    for (const r of timeframeRows) timeframeBreakdown[r.timeframe] = r.cnt;
    res.json({
      totalAnalyses: totals.totalAnalyses,
      buySignals: buyStats.count,
      sellSignals: sellStats.count,
      avgSuccessProbability: Math.round(Number(totals.avgSuccessProbability ?? 0)),
      avgBuyProbability: Math.round(Number(buyStats.avg ?? 0)),
      avgSellProbability: Math.round(Number(sellStats.avg ?? 0)),
      marketBreakdown,
      timeframeBreakdown,
    });
  } catch (err) {
    req.log.error({ err }, "Error fetching stats");
    res.status(500).json({ error: "server_error", message: "حدث خطأ في الخادم" });
  }
});

